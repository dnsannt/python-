# FUNGSI MATH (MATEMATIKA)
from math import *

# untuk membulatkan suatu bilangan desimal berkoma.
print(round(3.7))
# fungsi floor membulatkan angka ke bawah
print(floor(3.7))
# Metode math.ceil() membulatkan angka ke atas pada bilangan bulat terdekat
print(ceil(3.7))
# SQRT untuk mencari akar kuadrat
print(sqrt(36))